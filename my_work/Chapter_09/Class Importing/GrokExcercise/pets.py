class Dog:
    def __init__(self, name):
        self.name = name

    def bark(self):
        print(f"{self.name} says: Woof!")


class Cat:
    def __init__(self, name):
        self.name = name

    def meow(self):
        print(f"{self.name} says: Meow!")


class Bird:
    def __init__(self, name):
        self.name = name

    def chirp(self):
        print(f"{self.name} says: Chirp!")
